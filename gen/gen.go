var goPath string

func init() {
	cmd := exec.Command("go", "env", "GOPATH")
	b, err := cmd.Output()
	if err != nil {
		panic(err)
	}
	goPath = strings.Trim(string(b), "\n")
}

type Package struct {
	*packages.Package
	funcs []*Func
}

func NewPackage(pkg *packages.Package) *Package {
	return &Package{pkg, nil}
}

func (pkg *Package) PkgPathSegments() []string {
	return strings.Split(pkg.PkgPath, "/")
}

func (pkg *Package) Funcs() []*Func {
	if pkg.funcs == nil {
		pkg.funcs = []*Func{}
		scope := pkg.Types.Scope()
		for _, name := range scope.Names() {
			f, ok := scope.Lookup(name).(*types.Func)
			if !ok || !f.Exported() {
				continue
			}
			if f.Type().(*types.Signature).Recv() != nil {
				fmt.Printf("skipping %v because of receiver\n", f)
				continue
			}
			pkg.funcs = append(pkg.funcs, NewFunc(pkg, f))
		}
	}
	return pkg.funcs
}

type Func struct {
	*types.Func
	pkg    *Package
	params []*Var
}

func NewFunc(pkg *Package, f *types.Func) *Func {
	return &Func{f, pkg, nil}
}

func (f *Func) InternalName() string {
	internalName := f.Name()
	internalName = string(regexp.MustCompile("[^a-zA-Z0-9]").ReplaceAll([]byte(internalName), []byte{}))
	internalName = string(append([]rune{unicode.ToLower([]rune(internalName)[0])}, []rune(internalName)[1:]...))
	return internalName
}

func (f *Func) Params() []*Var {
	if f.params == nil {
		f.params = []*Var{}
		signature := f.Type().(*types.Signature)
		params := signature.Params()
		for i := 0; i < params.Len(); i++ {
			f.params = append(f.params, NewVar(f.pkg, params.At(i)))
		}
		if signature.Variadic() {
			f.params[len(f.params)-1].Variadic = true
		}
	}
	return f.params
}

func (f *Func) ParamTypes() string {
	var paramTypes []string
	for _, param := range f.Params() {
		if param.Variadic {
			paramTypes = append(paramTypes, fmt.Sprintf("...%v", param.Type().(*types.Slice).Elem()))
		} else {
			paramTypes = append(paramTypes, param.Type().String())
		}
	}
	return strings.Join(paramTypes, ", ")
}

func (f *Func) QualifiedName() string {
	return QualifiedName(f.FullName())
}

type Var struct {
	*types.Var
	pkg      *Package
	Variadic bool
}

func NewVar(pkg *Package, v *types.Var) *Var {
	return &Var{v, pkg, false}
}

func (v *Var) QualifiedType() string {
	if v.Variadic {
		return QualifiedName(v.Type().(*types.Slice).Elem().String())
	}
	return QualifiedName(v.Type().String())
}

func QualifiedName(name string) string {
	a := strings.Split(name, ".")
	if len(a) == 1 {
		return a[0]
	}
	b := strings.Split(a[0], "/")
	return fmt.Sprintf("%v.%v", b[len(b)-1], a[1])
}

var goFileTemplate = template.Must(template.New("goFile").Parse(`
// WARNING: this file was auto-generated by Grime

package {{.Name}}

import (
	{{.Name}} "{{.PkgPath}}"
	"fmt"
	"reflect"
	"strings"
	"github.com/katsuya94/grime/common"
	"github.com/katsuya94/grime/runtime"
)

var Library *runtime.Library = runtime.MustNewEmptyLibrary([]common.Symbol{
	{{range .PkgPathSegments -}}
	common.Symbol("{{.}}"),
{{end -}}
}, []int{})

var Bindings common.BindingSet

func init() {
	env := common.EmptyEnvironment
	{{range .Funcs -}}
	env = env.MustDefine(common.Symbol("{{.QualifiedName}}"), []int{0}, &common.Keyword{common.Function({{.InternalName}})})
	{{end -}}
	Bindings = env.Bindings()
}

{{range .Funcs}}
func {{.InternalName}}(c common.Continuation, args ...common.Datum) (common.EvaluationResult, error) {
	{{if len .Params -}}
	{{if .Type.Variadic -}}
	valid := len(args) >= {{len .Params}} - 1
	{{else -}}
	valid := len(args) == {{len .Params}}
	{{end -}}
	{{range $i, $param := .Params -}}
	{{if $param.Variadic -}}
	var {{$param.Name}} []{{$param.QualifiedType}}
	{{else -}}
	var {{$param.Name}} {{$param.QualifiedType}}
	{{end -}}
	if valid {
		{{if $param.Variadic -}}
		for _, arg := range args[{{$i}}:] {
			elem, ok := arg.({{$param.QualifiedType}})
			valid = valid && ok
			{{$param.Name}} = append({{$param.Name}}, elem)
		}
	{{else -}}
		var ok bool
		{{$param.Name}}, ok = args[{{$i}}].({{$param.QualifiedType}})
		valid = valid && ok
	{{end -}}
	}
	{{end -}}
	if !valid {
		var argTypes []string
		for _, arg := range args {
			argTypes = append(argTypes, reflect.TypeOf(arg).String())
		}
		return common.ErrorC(fmt.Errorf("{{.QualifiedName}}: expected ({{.ParamTypes}}) got (%v)", strings.Join(argTypes, ", ")))
	}
	{{end -}}
	{{.QualifiedName}}(
		{{range .Params -}}
		{{if .Variadic -}}
		{{.Name}}...,
	{{else -}}
		{{.Name}},
	{{end -}}
	{{end -}}
	)
	return common.CallC(c, common.Void)
}
{{end}}
`))

type GrimePackage struct {
	Name string
	Src  string
}

var grimeFileTemplate = template.Must(template.New("grimeFile").Parse(`
// WARNING: this file was auto-generated by Grime

// +build !js

package {{.Name}}

import "github.com/katsuya94/grime/runtime"

var Library *runtime.Library

func init() {
	Library = runtime.MustNewLibraryFromFile("{{.Name}}")
}
`))

var grimeJsFileTemplate = template.Must(template.New("grimeJsFile").Parse(`
// WARNING: this file was auto-generated by Grime

// +build js

package {{.Name}}

import "github.com/katsuya94/grime/runtime"

var Library *runtime.Library

func init() {
	Library = runtime.MustNewLibraryFromString("{{.Name}}", ` + "`" + `
{{.Src}}
` + "`" + `)
}
`))

func Generate(pattern string) error {
	ok, err := generateGrimePackage(pattern)
	if err != nil {
		return err
	} else if ok {
		return nil
	}
	ok, err = generateGoPackage(pattern)
	if err != nil {
		return err
	} else if ok {
		return nil
	}
	return fmt.Errorf("no matching package found")
}

func generateGrimePackage(pattern string) (bool, error) {
	pkg, err := loadPackage(generatedPackagePath(pattern))
	if err != nil {
		return false, err
	} else if pkg == nil {
		return false, nil
	}
	inferredPackageName := path.Base(pattern)
	filepath := path.Join(goPath, "src", pkg.PkgPath, fmt.Sprintf("%v.scm", inferredPackageName))
	f, err := os.Open(filepath)
	if os.IsNotExist(err) {
		return false, nil
	} else if err != nil {
		return false, err
	}
	b, err := ioutil.ReadAll(f)
	if err != nil {
		return false, err
	}
	data := &GrimePackage{
		Name: inferredPackageName,
		Src:  string(regexp.MustCompile("`").ReplaceAll(b, []byte("` + \"`\" + `"))),
	}
	buf := &bytes.Buffer{}
	err = grimeFileTemplate.Execute(buf, data)
	if err != nil {
		return false, err
	}
	err = parseCheckWrite(fmt.Sprintf("%v.go", inferredPackageName), pkg.PkgPath, buf.String())
	if err != nil {
		return false, err
	}
	buf.Reset()
	err = grimeJsFileTemplate.Execute(buf, data)
	if err != nil {
		return false, err
	}
	err = parseCheckWrite(fmt.Sprintf("%v_web.go", inferredPackageName), pkg.PkgPath, buf.String())
	if err != nil {
		return false, err
	}
	return true, nil
}

func generateGoPackage(pattern string) (bool, error) {
	pkg, err := loadPackage(pattern)
	if err != nil {
		return false, err
	} else if pkg == nil {
		return false, nil
	}
	buf := &bytes.Buffer{}
	err = goFileTemplate.Execute(buf, NewPackage(pkg))
	if err != nil {
		return false, err
	}
	err = parseCheckWrite(fmt.Sprintf("%v.go", pkg.Name), generatedPackagePath(pkg.PkgPath), buf.String())
	if err != nil {
		return false, err
	}
	return true, nil
}

func generatedPackagePath(pkgPath string) string {
	return fmt.Sprintf("github.com/katsuya94/grime/lib/%v", pkgPath)
}

func loadPackage(pattern string) (*packages.Package, error) {
	pkgs, err := packages.Load(&packages.Config{Mode: packages.LoadTypes}, pattern)
	if err != nil {
		return nil, err
	}
	if len(pkgs) == 0 {
		return nil, nil
	} else if len(pkgs) == 1 {
		return pkgs[0], nil
	}
	return nil, fmt.Errorf("found %v packages", len(pkgs))
}

func parseCheckWrite(filename string, pkgPath string, src string) error {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, filename, src, parser.ParseComments)
	if err != nil {
		fmt.Fprintln(os.Stderr, src)
		return err
	}
	conf := types.Config{Importer: importer.Default()}
	_, err = conf.Check(pkgPath, fset, []*ast.File{file}, nil)
	if err != nil {
		fmt.Fprintln(os.Stderr, src)
		return err
	}
	filepath := path.Join(goPath, "src", pkgPath, filename)
	err = os.MkdirAll(path.Dir(filepath), 0775)
	if err != nil {
		return err
	}
	f, err := os.OpenFile(filepath, os.O_WRONLY|os.O_CREATE|os.O_TRUNC, 0664)
	if err != nil {
		return err
	}
	err = format.Node(f, fset, file)
	if err != nil {
		return err
	}
	err = f.Close()
	if err != nil {
		return err
	}
	fmt.Printf("wrote %v\n", filepath)
	return nil
}
